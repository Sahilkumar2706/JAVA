class advance{
    public static void main(String args[])
    {
    int x=2;
    int y=9;
    int c= x+y;
    System.out.println("x+y ="+(c));
    int d=c++;
    System.out.println("c++ = "+(d));
    int modd=d%2;
    System.out.println("modd = "+(modd));
    int e= --modd;
    System.out.println("(--)e= "+(e));
    int j= x-y;
    System.out.println("x-y ="+(j));
    int k=j++;
    System.out.println("j++ = "+(k));
    int modde=k%2;
    System.out.println("modde = "+(modde));
    int l= --modde;
    System.out.println("(--)l= "+(l));
    int m= x*y;
    System.out.println("x*y ="+(m));
    int n=m++;
    System.out.println("n++ = "+(n));
    int moddl=d%2;
    System.out.println("moddl = "+(moddl));
    int o= --moddl;
    System.out.println("(--)o= "+(o));
    int p= x/y;
    System.out.println("x/y ="+(p));
    int q=p++;
    System.out.println("p++ = "+(q));
    int moddle=d%2;
    System.out.println("moddle = "+(moddle));
    int r= --moddle;
    System.out.println("(--)r= "+(r));
    
    }
    }
